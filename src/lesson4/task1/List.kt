 * 23964 = "двадцать три тысячи девятьсот шестьдесят четыре"
 */
fun russian(n: Int): String {
    val a = listOf(
        "один",
        "два",
        "три",
        "четыре",
        "пять",
        "шесть",
        "семь",
        "восемь",
        "девять",
        "",
        "двадцать",
        "тридцать",
        "сорок",
        "пятьдесят",
        "шестьдесят",
        "семьдесят",
        "восемьдесят",
        "девяносто",
        "сто",
        "двести",
        "триста",
        "четыреста",
        "пятьсот",
        "шестьсот",
        "семьсот",
        "восемьсот",
        "девятьсот"
    )
    var h = n
    var s = 0
    val r = mutableListOf<String>()
    var x = n
    var o: Int
    var k = 0
    if (n == 0) return "ноль"
    while (h != 0) {
        h /= 10
        s += 1
    }
    for (i in 1..s) {
        o = x % 10
        x /= 10
        if (k == 27) k = 0
        if ((x % 10 == 1) && (k == 0)) {
            if (i == 4) r.add("тысяч")
            when (o) {
                0 -> r.add("десять")
                1 -> r.add("одиннадцать")
                2 -> r.add("двенадцать")
                3 -> r.add("тринадцать")
                4 -> r.add("четырнадцать")
                5 -> r.add("пятнадцать")
                6 -> r.add("шестнадцать")
                7 -> r.add("семнадцать")
                8 -> r.add("восемнадцать")
                9 -> r.add("девятнадцать")
            }
        } else {
            if (i == 4) {
                when (o) {
                    1 -> r.add("тысяча")
                    2, 3, 4 -> r.add("тысячи")
                    else -> r.add("тысяч")
                }
            }
            when {
                o == 1 && i == 4 -> r.add("одна")
                o == 2 && i == 4 -> r.add("две")
                o == 1 && (i != 2 && i != 5) -> r.add(a[0 + k])
                o == 2 -> r.add(a[1 + k])
                o == 3 -> r.add(a[2 + k])
                o == 4 -> r.add(a[3 + k])
                o == 5 -> r.add(a[4 + k])
                o == 6 -> r.add(a[5 + k])
                o == 7 -> r.add(a[6 + k])
                o == 8 -> r.add(a[7 + k])
                o == 9 -> r.add(a[8 + k])
            }
        }
        k += 9
    }
    return r.reversed().joinToString(separator = " ")
}
